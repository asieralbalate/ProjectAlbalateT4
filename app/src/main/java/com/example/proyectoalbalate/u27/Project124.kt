class Vector{
    private val array = IntArray(5)
    init{charge()}

    private fun charge(){
        for (i in array.indices) {
            array[i] = Random.nextInt(0, 11)
        }
    }

    fun printArray(): String {
        var aux = ""
        for (element in array) {
            aux += "$element\n"
        }
        return aux
    }

    fun getMin(): Int {
        return array.minOrNull() ?: 0
    }

    fun getMax(): Int {
        return array.maxOrNull() ?: 0
    }
}